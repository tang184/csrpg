MapHandler.new_map_block = function(x, y, sample_num) {
	map_block = {};
	map_block.x = x;
	map_block.y = y;
	map_block.sample = MapHandler.map_samples[sample_num];
	return map_block;
};

MapHandler.new_map_sample = function(id, img_num, pass) {
	map_sample = {};
	var filename = "images/map/map_" + General.zeroPad(img_num, 3) + ".png";
	map_sample.image = ImageHandler.load(filename);
	map_sample.id = id;
	map_sample.pass = pass;
	return map_sample;
};

MapHandler.to_map_pos = function(pos) {
	var x = pos.x;
	var y = pos.y;
	return new Point(((x - (MapHandler.block_size / 2)) / MapHandler.block_size), ((y - (MapHandler.block_size / 2)) / MapHandler.block_size));
};
MapHandler.to_abs_pos = function(pos) {
	var x = pos.x;
	var y = pos.y;
	return new Point(((x * MapHandler.block_size) + (MapHandler.block_size / 2)), ((y * MapHandler.block_size) + (MapHandler.block_size / 2)));
};

MapHandler.draw = function() {
	var cx;
	var cy;
	var temp;
	
	cy = 0;
	while (cy < MapHandler.max_y) {
		cx = 0;
		while (cx < MapHandler.max_x) {
			temp = ControlHandler.to_char_pos(MapHandler.to_abs_pos(new Point(cx - 0.5, cy - 0.5)));
			DoubleBuff.ctx.drawImage(MapHandler.map_blocks[cy][cx].sample.image, temp.x, temp.y);
			cx = cx + 1;
		}
		cy = cy + 1;
	}
};